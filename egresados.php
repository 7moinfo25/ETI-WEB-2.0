<script>
  const swiper = new Swiper(".mySwiper", {
    effect: "coverflow",
    grabCursor: true,
    centeredSlides: true,
    slidesPerView: "auto",
    loop: true,
    coverflowEffect: {
      rotate: 50,
      stretch: 0,
      depth: 200,
      modifier: 1,
      slideShadows: true,
    },
    navigation: {
      nextEl: ".swiper-button-next",
      prevEl: ".swiper-button-prev",
    },
  });

  document.getElementById('searchInput-sw').addEventListener('keypress', (e) => {
    if (e.key === 'Enter') {
      searchImage();
    }
  });

  function searchImage() {
  const query = document.getElementById('searchInput-sw').value.toLowerCase().trim();
  const slides = swiper.slides;
  for (let i = 0; i < slides.length; i++) {
    const img = slides[i].querySelector('img');
    const caption = slides[i].querySelector('.caption')?.textContent.toLowerCase() || '';
    const imgName = img?.getAttribute('src').toLowerCase() || '';
    if (imgName.includes(query) || caption.includes(query)) {
      swiper.slideTo(i);
      return;
    }
  }
  showPopupMessage('No se encontró ninguna coincidencia.');
}

function showPopupMessage(message) {
  const popup = document.getElementById('popupMessage');
  popup.textContent = message;
  popup.classList.add('show');

  setTimeout(() => {
    popup.classList.remove('show');
  }, 2500); // se oculta después de 2.5 segundos
}

</script>
